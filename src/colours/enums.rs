impl ANSI256Colours {
    pub fn foreground<T>(&self, s: &T) -> String
    where T: ToString {
        format!(
            "\x1b[38:5:{}m",
            self.value()
        )
        + &s.to_string()
        + "\x1b[39m"
    }

    pub fn background<T>(&self, s: &T) -> String
    where T: ToString {
        format!(
            "\x1b[48:5:{}m",
            self.value()
        )
        + &s.to_string()
        + "\x1b[49m"
    }
}