#[allow(dead_code)]
pub enum ANSIIntensity {
    Normal,
    Bold,
    Faint,
}
#[allow(dead_code)]
impl HasValue<u8> for ANSIIntensity {

    fn value(&self) -> u8 {
        match self {
            Self::Normal    => 22_u8,
            Self::Bold      => 1_u8,
            Self::Faint     => 2_u8,
        }
    }

}
#[allow(dead_code)]
impl HasResetter for ANSIIntensity {

    fn resetter(&self) -> Self {
        Self::Normal
    }

}
impl ANSIApply for ANSIIntensity {}


